import { useMemo } from 'react'

interface FileIconProps {
  mimeType?: string
  className?: string
}

/**
 * FileIcon component - displays appropriate icon based on MIME type
 */
export function FileIcon({ mimeType, className = 'w-5 h-5' }: FileIconProps) {
  const icon = useMemo(() => {
    if (!mimeType) {
      return <DocumentIcon className={className} />
    }

    if (mimeType.startsWith('image/')) {
      return <PhotoIcon className={className} />
    }

    if (mimeType.startsWith('audio/')) {
      return <AudioIcon className={className} />
    }

    if (mimeType.startsWith('video/')) {
      return <VideoIcon className={className} />
    }

    if (mimeType.includes('javascript') || mimeType.includes('typescript')) {
      return <CodeIcon className={className} />
    }

    if (mimeType === 'text/markdown' || mimeType === 'text/plain') {
      return <TextIcon className={className} />
    }

    if (mimeType === 'application/zip' || mimeType === 'application/x-tar') {
      return <ArchiveIcon className={className} />
    }

    if (mimeType === 'application/pdf') {
      return <PdfIcon className={className} />
    }

    if (mimeType === 'application/json') {
      return <JsonIcon className={className} />
    }

    return <DocumentIcon className={className} />
  }, [mimeType, className])

  return icon
}

